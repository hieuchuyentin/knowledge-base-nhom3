    private void SaveResultToFile(String sFileName)
    {
        String stream;
        FileOutputStream fileOut;
        PrintWriter printFile;
        
        try
        {
            fileOut = new FileOutputStream(sFileName, false);
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Không ghi lại được kết quả", "Lỗi", ERROR_MESSAGE);
            return;
        }
        
        printFile = new PrintWriter(fileOut);
        
        stream = textAreaDieuTri.getText();
        
        printFile.write(stream);
        printFile.close();
        JOptionPane.showMessageDialog(this, "Lưu kết quả thành công", "Thông báo", INFORMATION_MESSAGE);
        
        return;
    }
