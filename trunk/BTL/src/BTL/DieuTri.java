    public boolean huongDanDieuTri(String maBenh){
    {
        FileReader fileReader;
        String stream;
        String xauHuongDan = "";
        BufferedReader bufferReader;
        StringTokenizer tokenString;
        Event s;
        
        try
        {
            fileReader = new FileReader("src\\Dieu tri\\" + maBenh + ".txt");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, "Không thể đọc file hướng dẫn điều trị", "Lỗi đọc file", ERROR_MESSAGE);
            return false;
        }
        bufferReader = new BufferedReader(fileReader);
        
        do
        {
            try
            {  
                stream = bufferReader.readLine();   
                if (stream == null)
                    break;
                xauHuongDan += stream;
                xauHuongDan += "\r\n";

                

            }
            catch(Exception e)
            {
                JOptionPane.showMessageDialog(null, "Lỗi đọc tệp sự kiện", "Lỗi khởi tạo", ERROR_MESSAGE);
                try
                {
                    bufferReader.close();
                }
                catch (Exception e1)
                {            
                }
                return false;
            }
        }
        while (true);
        textAreaDieuTri.setText(xauHuongDan);
        //textAreaDieuTri.setFocusable(true);
        try
        {
            bufferReader.close();
        }
        catch (Exception e)
        {            
        }
        return true;
    }
    }
